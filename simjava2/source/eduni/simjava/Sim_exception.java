package eduni.simjava;

/**
 * Top-level error thrown when an error in the simulation occurs.
 * @see eduni.simjava.Sim_stat_exception
 * @see eduni.simjava.distributions.Sim_parameter_exception
 * @version     1.0, 13 May 2002
 * @author      Costas Simatos
 */
public class Sim_exception extends Error {

  /**
   * Produce an error with a message.
   * @param name The error's message
   */
  public Sim_exception(String msg) {
    super(msg);
  }

  /**
   * PRoduce an error without a message..
   */
  public Sim_exception() {
    super();
  }
}
